typedef enum {
  ECMA48_MOD_NONE  = 0x00,
  ECMA48_MOD_SHIFT = 0x01,
  ECMA48_MOD_ALT   = 0x02,
  ECMA48_MOD_CTRL  = 0x04,
} ecma48_mod;

typedef enum {
  ECMA48_KEY_NONE,

  ECMA48_KEY_ENTER,
  ECMA48_KEY_TAB,
  ECMA48_KEY_BACKSPACE,
  ECMA48_KEY_ESCAPE,

  ECMA48_KEY_UP,
  ECMA48_KEY_DOWN,
  ECMA48_KEY_LEFT,
  ECMA48_KEY_RIGHT,

  ECMA48_KEY_INS,
  ECMA48_KEY_DEL,
  ECMA48_KEY_HOME,
  ECMA48_KEY_END,
  ECMA48_KEY_PAGEUP,
  ECMA48_KEY_PAGEDOWN,

  ECMA48_KEY_MAX, // Must be last
} ecma48_key;

#endif
